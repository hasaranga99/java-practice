
public class javaP1 {
    public static void main(String[] args) {
        int n = 4, k = 2;
        System.out.println("1 = "+ ++n );
        System.out.println("2 = "+ n );
        System.out.println("3 = "+ n++ );
        System.out.println("4 = "+ n );
        System.out.println("5 = "+ -n );
        System.out.println("6 = "+ n ); 
        System.out.println("7 = "+ --n );
        System.out.println("8 = "+ n );
        System.out.println("9 = "+ n-- ); 
        System.out.println("10 = "+ n );
        System.out.println("11 = "+ n + k + (n+k)); 
        System.out.println("12 = "+ n );
        System.out.println("13 = "+ k ); 
        System.out.println("14 = "+"" + n + " " + k ); 
        System.out.println("15 = "+ n ); 
        System.out.println("16 = "+ " " + n ); 
        System.out.println("17 = "+ " n" ); 
        System.out.println("18 = "+ "\n" ); 
        System.out.println("19 = "+ " n * n = "); //CAREFUL!
        System.out.println("20 = "+ n * n ); 
        System.out.println("21 = "+ 'n' );
        System.out.println(n);
    }
}
